def area(width, height):
    result = width * height
    return result

result = area(5, 6)
print(result)
result2 = area(6, 7)
print(result2)
result3 = area(9, 10)
print(result3)

def subtract(num1, num2):
    result = num1 - num2
    return result

result = subtract(5, 2)
print(result)
result2 = subtract(10, 8)
print(result2)

def divide(num1, num2):
    result = num1 / num2
    return result

def add(num1, num2):
    result = num1 + num2
    return result
print(add(3, 2))
print(add(99, 1))
